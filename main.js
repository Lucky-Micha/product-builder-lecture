function initTheme() {
    const isDark = localStorage.getItem('darkMode') === 'true';
    if (isDark) document.body.classList.add('dark-mode');
    updateThemeIcon(isDark);
}

function updateThemeIcon(isDark) {
    themeToggle.textContent = isDark ? '☀️' : '🌓';
}

themeToggle.addEventListener('click', () => {
    const isDark = document.body.classList.toggle('dark-mode');
    localStorage.setItem('darkMode', isDark);
    updateThemeIcon(isDark);
});

categoryBtns.forEach(btn => {
    btn.addEventListener('click', () => {
        categoryBtns.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        currentCategory = btn.dataset.category;
        recommendCafe();
    });
});

function recommendCafe() {
    if (!currentCategory) return;

    const list = cafeData[currentCategory];
    const randomIndex = Math.floor(Math.random() * list.length);
    const selected = list[randomIndex];

    displayResult(selected);
    addToHistory(selected, currentCategory);
}

function displayResult(cafe) {
    resultArea.style.display = 'block';
    
    // Use an error handler to detect broken images
    cafeImage.onerror = function() {
        this.src = "https://images.unsplash.com/photo-1501339847302-ac426a4a7cbb?q=80&w=1000&auto=format&fit=crop"; // Placeholder cafe image
    };
    
    cafeImage.src = cafe.img;
    cafeName.textContent = cafe.name;
    cafeDesc.textContent = cafe.desc;
    
    // UI for Map and Blog Links
    let linksHtml = `<p class="location">📍 <a href="${cafe.mapLink}" target="_blank" style="color: inherit; text-decoration: underline;">${cafe.location}</a></p>`;
    
    if (cafe.blogLinks && cafe.blogLinks.length > 0) {
        linksHtml += `<div class="blog-links" style="margin-top: 15px; text-align: left; font-size: 0.9rem;">
            <p style="font-weight: bold; margin-bottom: 5px;">🔗 인기 블로그 리뷰</p>
            <ul style="list-style: none; padding: 0;">`;
        cafe.blogLinks.forEach(blog => {
            linksHtml += `<li style="margin-bottom: 5px;">• <a href="${blog.url}" target="_blank" style="color: #666;">${blog.title}</a></li>`;
        });
        linksHtml += `</ul></div>`;
    }
    
    // Replace location content
    cafeLocation.innerHTML = linksHtml;
    
    resultArea.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
}

function addToHistory(cafe, category) {
    const categoryNames = { view: '풍경', architecture: '건축', reading: '독서' };
    const historyItem = { ...cafe, categoryName: categoryNames[category], time: new Date().toLocaleString() };
    
    if (historyList.length > 0 && historyList[0].name === cafe.name) return;

    historyList.unshift(historyItem);
    if (historyList.length > 5) historyList.pop();
    
    localStorage.setItem('cafeHistory', JSON.stringify(historyList));
    renderHistory();
}

function renderHistory() {
    historyUl.innerHTML = '';
    historyList.forEach(item => {
        const li = document.createElement('li');
        li.innerHTML = "<div><strong>" + item.name + "</strong><span class='history-tag'>" + item.categoryName + "</span></div><small style='font-size: 0.7rem; opacity: 0.7;'>" + item.time.split(',')[1] + "</small>";
        historyUl.appendChild(li);
    });
}

generateBtn.addEventListener('click', recommendCafe);

initTheme();
renderHistory();
